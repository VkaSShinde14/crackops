export const enrichedTopics = topics.map((topic) => ({
  ...topic,
  architecture: architectureByTopic[topic.id] ?? defaultArchitecture,
  prerequisites: topic.id === "aks" ? ["docker", "kubernetes", "pipelines"] : ["cicd", "git-branching"],
  relatedTopics: topics
    .filter((candidate) => candidate.id !== topic.id && candidate.category === topic.category)
    .slice(0, 3)
    .map((candidate) => candidate.id),
  relatedTools: [topic.id, topic.id === "pipelines" ? "agents" : "pipelines"].filter(Boolean),
}));

export function makeFollowUpTree(seed: string, context: string): FollowUpNode[] {
  return [
    {
      id: `${seed}-basic`,
      level: "basic",
      question: `Can you explain ${context} in one simple sentence?`,
      answer: `${context} should be explained as the practical problem it solves before naming components.`,
      sayIt: `I would start with the problem first, then map it to the tool and its impact.`,
      children: [
        {
          id: `${seed}-intermediate`,
          level: "intermediate",
          question: "How does it work internally during a real delivery flow?",
          answer: "Describe the trigger, identity, worker/runtime, artifact or state movement, approval boundary, and feedback loop.",
          sayIt: "In a real flow, I explain what starts it, where it runs, what it produces, and how it is promoted safely.",
          children: [
            {
              id: `${seed}-advanced`,
              level: "advanced",
              question: "What can fail in production and how would you troubleshoot it?",
              answer: "Check logs, identity scope, network reachability, artifact versions, environment approvals, and recent changes before applying a fix.",
              sayIt: "I troubleshoot from signal to dependency: logs first, then permissions, network, artifacts, and rollout history.",
              children: [
                {
                  id: `${seed}-architect`,
                  level: "architect",
                  question: "How would you design this for a large enterprise team?",
                  answer: "Use reusable templates, environment separation, least-privilege identities, policy checks, observability, cost controls, and ownership boundaries.",
                  sayIt: "At enterprise scale I design for governance and developer speed together, not one at the expense of the other.",
                },
              ],
            },
          ],
        },
      ],
    },
  ];
}

export const enrichedQuestions: Question[] = questions.map((question) => ({
  ...question,
  followUps: makeFollowUpTree(question.id, question.question.toLowerCase()),
}));

function interviewQuestion(id: string, topicId: string, question: string): Question {
  return {
    id,
    topicId,
    difficulty: "senior",
    question,
    shortAnswer: "Frame the tool by problem, workflow, controls, and tradeoffs.",
    detailedAnswer: "Explain where it sits in the delivery lifecycle, what components participate, how identity and artifacts move, and how you operate it when something fails.",
    seniorAnswer: "A senior answer includes governance, scaling, security, cost, observability, and migration tradeoffs.",
    interviewVersion: "I would answer with a real production flow, then call out failure points and the controls I use to prevent them.",
    followUps: makeFollowUpTree(id, question.toLowerCase()),
  };
}

